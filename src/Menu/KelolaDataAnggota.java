    private void txtIDKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtIDKeyReleased
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
                        if (txtID.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Silahkan isi kode anggota terlebih dahulu!", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            txtID.requestFocus();
            return;
            }
            dataDitemukan = cariData(txtID.getText());
            if (dataDitemukan == true) {
                int pilihan;
                pilihan = JOptionPane.showConfirmDialog(this, "Kode Anggota " + txtID.getText() + " sudah ada! " + "Apakah anda ingin mengubah/menghapus data ?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
                if (pilihan == 0) {
                    try {
                        txtNama.setText(rs.getString("nama_anggota"));
                        txtEmail.setText(rs.getString("email"));
                        txtTelepon.setText(rs.getString("telepon"));
                        cbxJenisKelamin.setSelectedItem(rs.getString("jenis_kelamin"));
                        txtTanggal.setText(rs.getString("tgl_join"));
                        txtID.setEnabled(false);
                        txtNama.setEnabled(false);
                        txtEmail.setEnabled(false);
                        txtTelepon.setEnabled(false);
                        btnBaru.setEnabled(false);
                        btnSimpan.setEnabled(false);
                        btnHapus.setEnabled(false);
                        btnUbah.requestFocus();
                        btnUbah.setEnabled(true);
                        btnBatal.setEnabled(true);
                    } catch (SQLException e) {
                        System.err.println("Query Error");
                        e.printStackTrace();
                    }
                } else {
                    txtID.setText("");
                    txtID.requestFocus();
                }
            } else {
                txtID.setEnabled(false);
                txtNama.setEnabled(true);
                txtEmail.setEnabled(true);
                txtTelepon.setEnabled(true);
                cbxJenisKelamin.setEnabled(true);
                txtNama.requestFocus();
                txtTanggal.setEnabled(true);
                btnSimpan.setEnabled(true);
                btnBatal.setEnabled(true);
            }
        }
    }//GEN-LAST:event_txtIDKeyReleased


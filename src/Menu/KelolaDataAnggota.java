    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        dataDitemukan = cariData(txtID.getText());
        emailDitemukan = cariDataEmail(txtEmail.getText());
        teleponDitemukan = cariDataTelepon(txtTelepon.getText());
        if (txtNama.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Silahkan lengkapi data nama anggota!", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            txtNama.requestFocus();
        } else if (txtEmail.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Silahkan lengkapi data email!", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            txtEmail.requestFocus();
        } else if (txtTelepon.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Silahkan lengkapi data telepon!", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            txtTelepon.requestFocus();
        } else if (cbxJenisKelamin.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "Silahkan pilih jenis kelamin!", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            cbxJenisKelamin.requestFocus();
        } else if (txtTanggal.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Silahkan lengkapi data tanggal bergabung!", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            txtTanggal.requestFocus();
        } else {
            if (modeSimpan) {

                if (dataDitemukan) {
                    JOptionPane.showMessageDialog(this, "Data gagal ditambahkan, " + "karena kode anggota " + txtID.getText() + " telah ada di tabel", "Informasi", JOptionPane.ERROR_MESSAGE);
                } else if (emailDitemukan) {
                    JOptionPane.showMessageDialog(this, "Data gagal ditambahkan, " + "karena email " + txtEmail.getText() + " telah digunakan", "Informasi", JOptionPane.ERROR_MESSAGE);
                    return;
                } else if (teleponDitemukan) {
                    JOptionPane.showMessageDialog(this, "Data gagal ditambahkan, " + "karena no telepon " + txtTelepon.getText() + " telah digunakan", "Informasi", JOptionPane.ERROR_MESSAGE);
                    return;
                } else {
                    try {

                        perintah.execute("INSERT INTO anggota VALUES('"
                                + txtID.getText() + "','"
                                + txtNama.getText() + "','"
                                + txtEmail.getText() + "','"
                                + txtTelepon.getText() + "','"
                                + cbxJenisKelamin.getSelectedItem() + "','"
                                + txtTanggal.getText() + "')"
                        );
                        JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan", "Informasi", JOptionPane.INFORMATION_MESSAGE);
                        tampilData();
                    } catch (SQLException e) {
                        JOptionPane.showMessageDialog(this, "Data gagal ditambahkan", "Informasi", JOptionPane.ERROR_MESSAGE);
                        e.printStackTrace();

                    }
                }
            } else { // mode update
                if ((tblData.getSelectedRow() == -1) && (dataDitemukan == false)) {
                    JOptionPane.showMessageDialog(this, "Silahkan pilih baris yang akan diubah!", "Informasi", JOptionPane.INFORMATION_MESSAGE);
                } else if (emailDitemukan) {
                    JOptionPane.showMessageDialog(this, "Data gagal diupdate, " + "karena email " + txtEmail.getText() + " telah digunakan", "Informasi", JOptionPane.ERROR_MESSAGE);
                    return;
                } else if (teleponDitemukan) {
                    JOptionPane.showMessageDialog(this, "Data gagal diupdate, " + "karena no telepon " + txtTelepon.getText() + " telah digunakan", "Informasi", JOptionPane.ERROR_MESSAGE);
                    return;
                } else {
                    try {
                        perintah.execute("UPDATE anggota SET "
                                + "nama_anggota = '" + txtNama.getText() + "', "
                                + "email = '" + txtEmail.getText() + "', "
                                + "telepon = '" + txtTelepon.getText() + "', "
                                + "jenis_kelamin = '" + cbxJenisKelamin.getSelectedItem() + "', "
                                + "tgl_join = '" + txtTanggal.getText() + "' "
                                + "WHERE kode_anggota = '" + txtID.getText() + "'");
                        JOptionPane.showMessageDialog(this, "Data berhasil diubah", "Informasi", JOptionPane.INFORMATION_MESSAGE);
                        tampilData();
                    } catch (SQLException e) {
                        System.err.println("Query Error");
                        e.printStackTrace();

                    }
                }
            }

            bersihData();
            txtID.setEnabled(false);
            txtNama.setEnabled(false);
            txtEmail.setEnabled(false);
            txtTelepon.setEnabled(false);
            cbxJenisKelamin.setEnabled(false);
            txtTanggal.setEnabled(false);
            btnBaru.requestFocus();
            btnBaru.setEnabled(true);
            btnSimpan.setEnabled(false);
            btnUbah.setEnabled(false);
            btnHapus.setEnabled(false);
            btnBatal.setEnabled(false);
        }
    }//GEN-LAST:event_btnSimpanActionPerformed


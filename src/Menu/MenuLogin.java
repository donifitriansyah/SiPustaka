    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        if (txtUsername.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Username tidak boleh kosong!");
        } else if (new String(txtPassword.getPassword()).isEmpty()) {
            JOptionPane.showMessageDialog(this, "Password tidak boleh kosong!");
        } else {
            String username = txtUsername.getText();
            String password = new String(txtPassword.getPassword());
            String passwordTerenkripsi = enkripsiSHA256(password);

            if (cekLogin(username, passwordTerenkripsi)) {
                MainMenu mm = new MainMenu(kodeUser, role, namaUser, userName, passwordUser);
                mm.setVisible(true);
                dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Login gagal. Periksa kembali username dan password.", "Informasi", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnLoginActionPerformed


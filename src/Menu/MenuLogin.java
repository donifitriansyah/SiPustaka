    public String enkripsiSHA256(String pass) {
        String digest = null;
        try {
            MessageDigest md = MessageDigest.getInstance("SHA-256");
            byte[] hash = md.digest(pass.getBytes("UTF-8"));

            StringBuilder sb = new StringBuilder(2 * hash.length);
            for (byte b : hash) {
                sb.append(String.format("%02x", b & 0xff));
            }
            digest = sb.toString();
        } catch (Exception e) {
            e.printStackTrace();
        }
        return digest;
    }

    private boolean cekLogin(String username, String passwordTerenkripsi) {
        if (koneksi != null) {
            try {
                String query = "SELECT * FROM user WHERE username=? AND password=?";
                try (PreparedStatement ps = koneksi.prepareStatement(query)) {
                    ps.setString(1, username);
                    ps.setString(2, passwordTerenkripsi);

                    try (ResultSet rs = ps.executeQuery()) {
                        if (rs.next()) {
                            kodeUser = rs.getString("kode_user");
                            role = rs.getString("role");
                            namaUser = rs.getString("nama_lengkap");
                            userName = rs.getString("username");
                            passwordUser = rs.getString("password");

                            return true;
                        }
                    }
                }
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
        return false;
    }


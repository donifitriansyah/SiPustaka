    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        dataDitemukan = cariData(txtKode.getText());
        usernameDitemukan = cariDataUsername(txtUser.getText());
        if (txtNama.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Silahkan lengkapi data nama petugas!", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            txtNama.requestFocus();
        } else if (txtUser.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Silahkan lengkapi data username!", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            txtUser.requestFocus();
        } else if (cbxRole.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "Silahkan pilih role user!", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            cbxRole.requestFocus();
        } else {
            if (modeSimpan) {

                if (dataDitemukan) {
                    JOptionPane.showMessageDialog(this, "Data gagal ditambahkan, " + "karena kode user " + txtKode.getText() + " telah ada di tabel", "Informasi", JOptionPane.ERROR_MESSAGE);
                } else if (usernameDitemukan) {
                    JOptionPane.showMessageDialog(this, "Data gagal ditambahkan, " + "karena username " + txtUser.getText() + " telah digunakan", "Informasi", JOptionPane.ERROR_MESSAGE);
                    return;
                } else {
                    try {
                        String DefaultPassword = login.enkripsiSHA256("sipustaka");
                        perintah.execute("INSERT INTO user VALUES('"
                                + txtKode.getText() + "','"
                                + txtNama.getText() + "','"
                                + txtUser.getText() + "','"
                                + DefaultPassword + "','"
                                + cbxRole.getSelectedItem() + "')"
                        );
                        JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan", "Informasi", JOptionPane.INFORMATION_MESSAGE);
                        tampilData();
                    } catch (SQLException e) {
                        JOptionPane.showMessageDialog(this, "Data gagal ditambahkan", "Informasi", JOptionPane.INFORMATION_MESSAGE);
                        e.printStackTrace();

                    }
                }
            } else { // mode update
                if ((tblData.getSelectedRow() == -1) && (dataDitemukan == false) && (usernameDitemukan == false)) {
                    JOptionPane.showMessageDialog(this, "Silahkan pilih baris yang akan diubah!", "Informasi", JOptionPane.INFORMATION_MESSAGE);
                } else if (usernameDitemukan) {
                    JOptionPane.showMessageDialog(this, "Data gagal diupdate, " + "karena username " + txtUser.getText() + " telah digunakan", "Informasi", JOptionPane.INFORMATION_MESSAGE);
                    return;
                } else {
                    try {
                        perintah.execute("UPDATE user SET "
                                + "nama_lengkap = '" + txtNama.getText() + "', "
                                + "username = '" + txtUser.getText() + "', "
                                + "role = '" + cbxRole.getSelectedItem() + "' "
                                + "WHERE kode_user = '" + txtKode.getText() + "'");
                        JOptionPane.showMessageDialog(this, "Data berhasil diubah", "Informasi", JOptionPane.INFORMATION_MESSAGE);
                        tampilData();
                    } catch (SQLException e) {
                        System.err.println("Query Error");
                        e.printStackTrace();

                    }
                }
            }

            bersihData();
            txtKode.setEnabled(false);
            txtNama.setEnabled(false);
            txtUser.setEnabled(false);
            cbxRole.setEnabled(false);
            btnBaru.requestFocus();
            btnBaru.setEnabled(true);
            btnSimpan.setEnabled(false);
            btnUbah.setEnabled(false);
            btnHapus.setEnabled(false);
            btnBatal.setEnabled(false);
            btnReset.setEnabled(false);
        }
    }//GEN-LAST:event_btnSimpanActionPerformed


    private void txtKodeKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtKodeKeyReleased

        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (txtKode.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Silahkan isi kode petugas terlebih dahulu!", "Informasi", JOptionPane.INFORMATION_MESSAGE);
                txtKode.requestFocus();
                return;
            }
            dataDitemukan = cariData(txtKode.getText());
            if (dataDitemukan == true) {
                int pilihan;
                pilihan = JOptionPane.showConfirmDialog(this, "Kode user " + txtKode.getText() + " sudah ada! " + "Apakah anda ingin mengubah/menghapus data ?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
                if (pilihan == 0) {
                    try {
                        txtNama.setText(rs.getString("nama_lengkap"));
                        txtUser.setText(rs.getString("username"));
                        cbxRole.setSelectedItem(rs.getString("role"));
                        txtKode.setEnabled(false);
                        txtNama.setEnabled(false);
                        txtUser.setEnabled(false);
                        cbxRole.setEnabled(false);
                        btnBaru.setEnabled(false);
                        btnSimpan.setEnabled(false);
                        btnHapus.setEnabled(false);
                        btnUbah.requestFocus();
                        btnUbah.setEnabled(true);
                        btnBatal.setEnabled(true);
                    } catch (SQLException e) {
                        System.err.println("Query Error");
                        e.printStackTrace();
                    }
                } else {
                    txtKode.setText("");
                    txtKode.requestFocus();
                }
            } else {
                txtKode.setEnabled(false);
                txtNama.setEnabled(true);
                txtUser.setEnabled(true);
                txtNama.requestFocus();
                cbxRole.setEnabled(true);
                btnSimpan.setEnabled(true);
                btnBatal.setEnabled(true);
            }
        }
    }//GEN-LAST:event_txtKodeKeyReleased


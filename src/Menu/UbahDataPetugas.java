    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed

        usernameDitemukan = cariDataUsername(txtUser.getText());
        if (txtNama.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Silahkan lengkapi data nama user!", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            txtNama.requestFocus();
            return;
        } else if (txtUser.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Silahkan lengkapi data username!", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            txtUser.requestFocus();
            return;
        } else if (txtPassword.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Silahkan lengkapi data password!", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            txtUser.requestFocus();
            return;
        } else if (usernameDitemukan) {
            JOptionPane.showMessageDialog(this, "Data gagal diubah, " + "karena username " + txtUser.getText() + " telah digunakan", "Informasi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            String cekPassword = (txtPassword.getText().equals("********") ? passwordUser : login.enkripsiSHA256(txtPassword.getText()));
            perintah.execute("UPDATE user SET "
                    + "nama_lengkap = '" + txtNama.getText() + "', "
                    + "username = '" + txtUser.getText() + "', "
                    + "password = '" + cekPassword + "' "
                    + "WHERE kode_user = '" + txtKode.getText() + "'");
            JOptionPane.showMessageDialog(this, "Data berhasil diubah, aplikasi akan keluar otomatis", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            System.exit(0);
        } catch (Exception e) {
        }

    }//GEN-LAST:event_btnSimpanActionPerformed


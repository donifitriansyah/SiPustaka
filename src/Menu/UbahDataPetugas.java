    private boolean cariDataUsername(String data) {
        if (!txtUser.getText().equals("")) {
            try {
                rs = perintah.executeQuery("SELECT * FROM user WHERE kode_user!='" + txtKode.getText() + "' AND username LIKE BINARY '" + txtUser.getText() + "'");

                if (rs.next()) {
                    return true;
                } else {
                    return false;
                }
            } catch (SQLException e) {
                System.err.println("Query Error");
                e.printStackTrace();
            }
        }
        return false;
    }

